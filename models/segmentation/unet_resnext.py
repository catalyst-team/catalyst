class DecoderBlock(nn.Module):
    def __init__(self, in_channels, n_filters, is_deconv=False, scale=True):
        super().__init__()

        # B, C, H, W -> B, C/4, H, W
        self.conv1 = nn.Conv2d(in_channels, in_channels // 4, 1)
        self.norm1 = nn.BatchNorm2d(in_channels // 4)
        self.relu1 = nonlinearity(inplace=True)

        if scale:
            # B, C/4, H, W -> B, C/4, H, W
            if is_deconv:
                self.upscale = nn.ConvTranspose2d(in_channels // 4, in_channels // 4, 3,
                                                  stride=2, padding=1, output_padding=1)
            else:
                self.upscale = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
        else:
            self.upscale = nn.Conv2d(in_channels // 4, in_channels // 4, 3, padding=1)
        self.norm2 = nn.BatchNorm2d(in_channels // 4)
        self.relu2 = nonlinearity(inplace=True)

        # B, C/4, H, W -> B, C, H, W
        self.conv3 = nn.Conv2d(in_channels // 4, n_filters, 1)
        self.norm3 = nn.BatchNorm2d(n_filters)
        self.relu3 = nonlinearity(inplace=True)

    def forward(self, x):
        x = self.conv1(x)
        x = self.norm1(x)
        x = self.relu1(x)
        x = self.upscale(x)
        x = self.norm2(x)
        x = self.relu2(x)
        x = self.conv3(x)
        x = self.norm3(x)
        x = self.relu3(x)
        return x


class UNetSeResnext50(nn.Module):
    def __init__(self, num_classes, num_channels=3,
                 dropout_2d=0.2, last_filters=32):
        super().__init__()
        self.num_classes = num_classes
        self.num_channels = num_channels
        self.dropout_2d = nn.Dropout2d(p=dropout_2d)

        senet = se_resnext50_32x4d()
        n_filters = [128, 256, 512, 1024, 2048]
        is_deconv = True

        # Encoders
        self.encoders = nn.ModuleList([
            senet.layer0,
            senet.layer1,
            senet.layer2,
            senet.layer3,
            senet.layer4
        ])

        # Decoders
        self.decoders = nn.ModuleList([DecoderBlock(n_filters[0]+64, n_filters[0],
                                                    is_deconv, True)])
        for i in range(1, len(n_filters) - 1):
            scale = True if i != 1 else False
            self.decoders.append(DecoderBlock(2 * n_filters[i], n_filters[i - 1],
                                              is_deconv, scale))
        self.decoders.append(DecoderBlock(n_filters[-1], n_filters[-2],
                                          is_deconv, True))

        self.finalconv2 = nn.Conv2d(n_filters[0], last_filters, 3, padding=1)
        self.finalrelu2 = nonlinearity(inplace=True)
        self.finalconv3 = nn.Conv2d(last_filters, num_classes, 3, padding=1)

    def forward(self, x):
        enc = []
        for encoder in self.encoders:
            x = encoder(x)
            enc.append(x)

        # Decoder with Skip Connections
        for i in range(len(self.decoders) - 1, 0, -1):
            x = self.decoders[i](x)
            x = torch.cat([x, self.dropout_2d(enc[i - 1])], dim=1)

        x = self.decoders[0](x)

        if self.dropout_2d is not None:
            x = self.dropout_2d(x)
        x = self.finalconv2(x)
        x = self.finalrelu2(x)
        x = self.finalconv3(x)

        return x


